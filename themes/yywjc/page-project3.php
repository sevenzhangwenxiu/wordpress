                <h3>斜视的定义</h3>
                <p class="make_word">斜视是除屈光不正（近视、远视、散光）及弱视之外，孩子最常见的眼科疾病之一。我们常听到的“斜眼”、“对眼”、“斗鸡眼”都是它的别称。</p>
                <p class="make_word">斜视的严格定义，指的是双眼无法同时注视目标，即两只眼睛的视轴不平行。根据眼位偏斜方向的不同，它可以被分成四个类型：内斜视、外斜视、上斜视、下斜视。</p>
                <p class="make_word">孩子如果侧着身子或脑袋看东西，可注视的时候视轴是平行的，那就意味着并非是斜视。也就是说，孩子侧身看东西和斜视之间并非是等号关系，不能混为一谈。</p>
                <h4>科学判断是否斜视</h4>
                <p class="make_word">那么怎么科学判断出手否‍斜视呢？教大家一个角膜反光法，对斜视做初步的筛选：</p>
                <p class="make_word">家长和孩子面对面端正坐位，在33cm或40cm左右的距离，手持一个手电筒水平照在孩子两眼正中央的鼻梁处，让孩子盯着手电筒看。这时，电筒在孩子的两只眼睛上各自出现一个反光点。</p><img src="<?php bloginfo('template_directory');?>/images/project-02-02.jpg" alt="...">
				<img src="<?php bloginfo('template_directory');?>/images/project-02-03.jpg" alt="..." style="margin-left: 20px"><p class="make_word" style="padding-top:32px;"> <strong >看反光点是否在两只瞳孔（黑眼珠中央）的正当中。</strong></p>
                <p class="make_word">如果反光点在瞳孔中央，为眼睛正位；</p>
                <p class="make_word">如果反光点在瞳孔鼻侧方，为外斜视；</p>
                <p class="make_word">如果反光点在瞳孔颞侧方，则为内斜视；</p>
                <p class="make_word">总之，如果有任何一个反光点是在黑眼珠内侧、外侧或者偏上下（总之没有在当中），小心了，孩子可能就存在斜视。</p>
                <p class="make_word">此法简单易行，但小于8°的隐性斜视不易察觉，需要专业医生检查定性定量诊断。</p>
                <p class="make_word">斜视不仅引起弱视，而且影响双眼单视功能（立体视等）的发育与建立。斜视多于儿童时期发病，还影响到外观与美容，给孩子的生理与心理的发育发展造成了不良影响。早发现，早治疗，我院通过戴镜、同视机视功能训练、压贴三棱镜等非手术疗法，极大地提高了斜视的治愈率，减少复发率。</p>

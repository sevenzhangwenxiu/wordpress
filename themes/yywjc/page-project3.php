			<img src="<?php bloginfo('template_directory');?>/images/project-02-00.jpg" alt="..."><h2 style="padding:10px 0px;">弱视</h2>
            <h3>一、什么是弱视？</h3>
            <p class="make_word">一般眼科检查无器质性病变，以功能性因素为主所引起的远近视力低于0.9，且不能用镜片矫正者，均列为弱视。</p>
            <p class="make_word">弱视是由于先天或视觉发育的关键期进入眼内的光刺激不够充分，剥夺了黄斑形成清晰物象的机会（视觉剥夺）和（或）两眼视觉输入不等引起清晰物象与模糊物象间发生竞争（双眼相互作用异常）所造成的单眼或双眼视力减退。</p>
            <p class="make_word">大量研究资料证明，弱视实际上是一种与双眼有关的病情，是视觉发育早期双眼失去平衡的结果，根据这种理论，要注意患者的双眼功能失调，而不单是单眼视力的低下。</p>
            <h3>二、弱视的分类</h3>
            <p>1、按视力降低程度分类</p>
            <p class="make_word">我国斜视弱视防治学组按矫正远视力把弱视分为轻、中、重3种程度。</p>
            <p class="make_word">轻度：矫正视力0.6-0.8</p>
            <p class="make_word">中度：矫正视力0.2-0.5</p>
            <p class="make_word">重度：矫正视力≤0.1。</p>
            <p>2、按病因分类</p>
            <p class="make_word">(1)屈光不正性弱视：多为双侧性，发生在屈光不正未矫正患者，视觉系统未得到清晰的视觉影像刺激，发育障碍成为弱视。配戴合适的矫正眼镜后，视力可逐渐提高。常见于远视≥3.00D，近视＞-6.00D，散光＞±1.50D。
            </p>
            <p class="make_word">(2)屈光参差性弱视：一眼或两眼有屈光不正，两眼屈光参差较大（球镜差≥2.00D，柱镜差≥1.00D）致使视网膜成像大小不等，融合困难，视皮质中枢只能抑制屈光不正较重的一眼，日久便形成弱视。</p>
            <p class="make_word">(3)斜视性弱视：斜视患者由于物像在两眼不落到正常视网膜对应点上，引起复视和视混淆，患者感到极度不适，大脑皮质中枢主动抑制由斜视眼传入的视觉冲动，使黄斑功能长期被抑制而形成弱视。</p>
            <p class="make_word">(4)形觉剥夺性弱视：在婴幼儿期，由于角膜混浊、先天性或外伤性白内障、上睑下垂等原因，因缺少视觉刺激，而视功能发育受抑制，尤其在生后头3个月，形觉剥夺可形成严重弱视，其视力预后较斜视性、屈光参差性弱视更为严重。</p>
            <p class="make_word">(5)其他原因引起的弱视：先天性弱视、中毒性弱视、营养性弱视、癔病性弱视、中枢神经损伤性弱视（窒息）等。包括：①器质性弱视，如新生儿视网膜、黄斑、视路出血；②微小眼球震颤。因发生在婴儿出生时，视功能尚未发育故预后极差。</p>
            <h3>三、弱视的临床表现</h3>
            <p>1、远视力与近视力减退：其最佳矫正视力达不到该年龄段的正常视力。</p>
            <p>2、拥挤现象：分辨排列成行视标的能力较单个视标差。</p>
            <p>3、注视性质：弱视眼可有固视不良，常见于斜视性弱视，表现为旁中心注视。</p>
            <p>4、视觉电生理改变：PVEP潜伏期延长，波幅下降。</p>
            <p>5、双眼单视功能：弱视不但中心视力低下，更重要的是还存在双眼单视功能的障碍。</p>
            <h3>四、弱视的危害</h3>
            <p>1、视力差</p>
            <p class="make_word">弱视患儿视力低下，如果不及时治疗，会导致视力永久性低下，影响后面的学习、生活和择业。伴有斜视者，影响患儿容貌的美观，易产生性格孤僻、自卑感。</p>
            <p>2、双眼视功能异常</p>
            <p class="make_word">弱视患儿如果没有完善的视觉功能，没有精细的立体视觉，难以从事工程设计、建筑、美工、驾驶、操作精密仪器、显微手术、参军、飞行员等工作。</p>
            <h3>五、弱视与近视的区别</h3>
            <p class="make_word">弱视和近视有着本质的不同，近视眼是由于眼调节肌肉睫状肌过度紧张或遗传等原因造成眼轴变长引起的看远模糊，通过戴眼镜可以矫正视力到正常。</p>
            <p class="make_word">而弱视是一种视功能发育迟缓，常伴有斜视、高度屈光不正，即使配戴眼镜视力也不能达到正常，往往需要配合视觉训练提高视力和双眼视功能，如果不及早发现、治疗，年龄越大，效果越差，易造成终生的视力低下、立体盲。</p>
            <h3>六、如何判断是否患有弱视？</h3>
            <p>1、怕光或眼位异常，或有眼球活动异常，如斜视、眼球震颤等；</p>
            <p>2、手眼协调能力较差，且易碰撞或跌倒；</p>
            <p>3、看电视歪头，甚至离电视越走越近；</p>
            <p>4、阅读时常看错行，看书有重影；</p>
            <p>5、视物眯眼或歪头。</p>
            <p>6、有些患儿的外观并无改变，看起来和正常人一样，极难发现。因此，父母要定期带孩子到医院找专业的眼科医生检查。</p>
            <h3>七、发现弱视了，该如何处理治疗呢？</h3>
            <p>1、及时到医院就诊，做相应的检查，如：远（近）视力检查、散瞳验光、电脑验光、远（近）立体视检查、眼位检查、眼前段裂隙灯检查、眼底检查等。</p>
            <p>2、根据医嘱配戴眼镜，必要时进行视功能训练，定期复查。</p>
            <p>3、注意用眼卫生，饮食均衡，睡眠充足，多做户外活动，预防眼部意外伤害。</p>
            <h3>八、常用治疗弱视的方法有哪些？</h3>
            <p>1、配镜治疗</p>
            <p>2、遮盖疗法，遮盖治疗是旁中心注视性弱视最有效的方法，须在医生指导下进行遮盖。</p>
            <p>3、红色滤光片和闪烁红光疗法，可迫使黄斑中心凹注视，从而抑制旁中心注视点。</p>
            <p>4、后像疗法，利用后像的痕迹反射引起回忆性反射而提高视力。</p>
            <p>5、光刷训练，旋转毛刷样影象的中心点相当于黄斑中心凹，宽窄不同的旋转影象可以刺激视中枢不用类型细胞，消除抑制和建立正常的相互联系。</p>
            <p>6、同视机视功能训练，包括同时视训练、融合训练、立体视训练。
            双眼视功能训练可以促进弱视儿童的视力提高，而且改善、恢复弱视儿童正常的双眼视功能，才能使增视效果巩固下来，减少斜弱视的复发。</p>
            <p>7、综合疗法：把不用的传统弱视单项治疗方法综合在一起以增进治疗效果。</p>
            <h4>九、怎样预防弱视的复发？</h4>
            <p>1、在治疗结束前，两眼的视力最好相等或相近，弱视眼的视力达到1.0或更好，然后再改为部分遮盖，适当延长治疗时间，巩固治疗效果。</p>
            <p>2、坚持配戴眼镜。即使裸眼视力达到1.0以上，若随意摘掉中度或以上远视眼镜，就有可能由于视疲劳、调节障碍引起视网膜影象模糊，最终导致弱视复发。</p>
            <p>3、准确把握斜视矫正时机，一旦弱视眼经治疗视力提高或接近健眼即行手术矫正斜视，并同步作双眼视功能训练，争取恢复融合功能，消除抑制。</p>
            <p>4、残余斜视是产生抑制的重要因素，即使是10∆之内的斜视，也可能够产生中心凹抑制，10∆之上的残余斜视，原来的弱视眼实际上仍处于“弱视状态”。</p>
            <p>5、中、重度弱视多伴有双眼单视异常，治疗的中后期，在继续其形觉刺激等综合弱视治疗的同时，重视加强双眼视觉的训练。</p>

